package java_module.java_basics.variables_and_literals;

public class LiteralsOfInteger {
    public static void main(String[] args) {
        // binary
        int binaryNumber = 0b1011111;  // 0b represents binary

        // octal
        int octalNumber = 0137;

        // decimal
        int decimalNumber = 95;

        // hexadecimal
        int hexNumber = 0x5F;  // 0x represents hexadecimal

        System.out.println(binaryNumber);  // prints 95
        System.out.println(octalNumber);  // prints 95
        System.out.println(decimalNumber);  // prints 95
        System.out.println(hexNumber);  // prints 95
    }
}
